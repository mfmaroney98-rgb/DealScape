DO $$ 
BEGIN
    IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME='seller_listings' AND COLUMN_NAME='naics_codes') THEN
        ALTER TABLE public.seller_listings ADD COLUMN naics_codes TEXT[] DEFAULT '{}'::text[];
    END IF;
END $$;

CREATE TABLE IF NOT EXISTS public.naics_industry_groups (
    code character varying NOT NULL,
    name character varying NOT NULL,
    subsector_code character varying,
    CONSTRAINT naics_industry_groups_pkey PRIMARY KEY (code),
    CONSTRAINT naics_industry_groups_subsector_code_fkey FOREIGN KEY (subsector_code) REFERENCES public.naics_subsectors(code)
);

ALTER TABLE public.naics_industry_groups ENABLE ROW LEVEL SECURITY;

DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_policy WHERE polname = 'Allow public read access to naics_industry_groups') THEN
        CREATE POLICY "Allow public read access to naics_industry_groups" ON public.naics_industry_groups FOR SELECT USING (true);
    END IF;
END $$;

